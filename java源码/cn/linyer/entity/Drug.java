package cn.linyer.entity;

/**
 * @author Linyer(韩啸翔)
 * 药品信息
 */
public class Drug {
	private String ypNo = null;
	private String ypName = null;
	private String ypEffect = null;
	private String ypPrice = null;
	private String ypDate = null;
	private String ypType = null;
	private String gysNo = null;
	
	public String getYpNo() {
		return ypNo;
	}
	public void setYpNo(String ypNo) {
		this.ypNo = ypNo;
	}
	public String getYpName() {
		return ypName;
	}
	public void setYpName(String ypName) {
		this.ypName = ypName;
	}
	public String getYpEffect() {
		return ypEffect;
	}
	public void setYpEffect(String ypEffect) {
		this.ypEffect = ypEffect;
	}
	public String getYpPrice() {
		return ypPrice;
	}
	public void setYpPrice(String ypPrice) {
		this.ypPrice = ypPrice;
	}
	public String getYpDate() {
		return ypDate;
	}
	public void setYpDate(String ypDate) {
		this.ypDate = ypDate;
	}
	public String getYpType() {
		return ypType;
	}
	public void setYpType(String ypType) {
		this.ypType = ypType;
	}
	public String getGysNo() {
		return gysNo;
	}
	public void setGysNo(String gysNo) {
		this.gysNo = gysNo;
	}
}
