package cn.linyer.entity;

/**
 * @author Linyer(韩啸翔)
 * 入库信息
 */
public class RuKu {
	private String rkNo = null;
	private String ypNo = null;
	private String rkQua = null;
	private String rkDate = null;
	
	public String getRkNo() {
		return rkNo;
	}
	public void setRkNo(String rkNo) {
		this.rkNo = rkNo;
	}
	public String getYpNo() {
		return ypNo;
	}
	public void setYpNo(String ypNo) {
		this.ypNo = ypNo;
	}
	public String getRkQua() {
		return rkQua;
	}
	public void setRkQua(String rkQua) {
		this.rkQua = rkQua;
	}
	public String getRkDate() {
		return rkDate;
	}
	public void setRkDate(String rkDate) {
		this.rkDate = rkDate;
	}
}
