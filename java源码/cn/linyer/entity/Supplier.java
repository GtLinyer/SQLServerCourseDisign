package cn.linyer.entity;

/**
 * @author Linyer(韩啸翔)
 * 供应商信息
 */
public class Supplier {
	private String gysNo = null;
	private String gysName = null;
	private String gysPhone = null;
	private String gysAddress = null;
	
	public String getGysNo() {
		return gysNo;
	}
	public void setGysNo(String gysNo) {
		this.gysNo = gysNo;
	}
	public String getGysName() {
		return gysName;
	}
	public void setGysName(String gysName) {
		this.gysName = gysName;
	}
	public String getGysPhone() {
		return gysPhone;
	}
	public void setGysPhone(String gysPhone) {
		this.gysPhone = gysPhone;
	}
	public String getGysAddress() {
		return gysAddress;
	}
	public void setGysAddress(String gysAddress) {
		this.gysAddress = gysAddress;
	}
}
